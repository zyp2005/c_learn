// divide.c -- 演示除法
#include <stdio.h>
int main(void)
{
    printf("integer divition: 5/4 is %d \n", 5 / 4);
    printf("integer divition: 6/3 is %d \n", 6 / 3);
    printf("integer divition: 7/4 is %d \n", 7 / 4);
    printf("floating divition: 7./4. is %1.2f \n", 7. / 4.);
    printf("mixed divition: 7./4 is %1.2f \n", 7. / 4);

    return 0;
}
