/* 两数的调和平均数这样计算：先得到两数的倒数，然后计算两个倒       */
/* 数的平均值，最后取计算结果的倒数。编写一个函数，接受两个double类 */
/* 型的参数，返回这两个参数的调和平均数。                           */
/********************************************************************/
#include <stdio.h>
double HarmonicMean(double x, double y);

double HarmonicMean(double x, double y)
{
    return 1 / ((1/x + 1/ y) / 2);
}
